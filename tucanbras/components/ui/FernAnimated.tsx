// ─── Props ────────────────────────────────────────────────────────────────────

interface Props {
  className?:           string
  style?:               React.CSSProperties
  // Whole-fern sway (pivot at bottom-left — base of stem)
  stemMaxDegrees?:      number  // max rotation of entire fern
  stemSmoothing?:       number  // lerp speed for whole-fern (lower = slower)
  // Per-leaf flutter
  leafMaxDegrees?:      number  // max flutter angle at tip
  leafMinDegrees?:      number  // max flutter angle at base
  leafTipSmoothing?:    number  // lerp speed at tip — reacts fastest (wave starts here)
  leafBaseSmoothing?:   number  // lerp speed at base — reacts slowest (wave ends here)
  // Shared scroll params
  velocitySensitivity?: number  // scroll px → degrees multiplier
  decay?:               number  // how fast target fades back to 0 per frame (0–1)
}

// ─── Leaf metadata ────────────────────────────────────────────────────────────
// [id suffix, is right-side leaf, position along stem: 0 = tip, 1 = base]
// Right-side leaves extend upper-right  → pivot at left  edge (transform-origin: 0% 50%)
// Left-side leaves  extend lower-left   → pivot at right edge (transform-origin: 100% 50%)

const LEAVES: [string, boolean, number][] = [
  ['01', true,  0.00],
  ['02', false, 0.07],
  ['03', true,  0.14],
  ['04', false, 0.21],
  ['05', true,  0.28],
  ['06', false, 0.35],
  ['07', true,  0.42],
  ['08', false, 0.49],
  ['09', true,  0.56],
  ['10', false, 0.63],
  ['11', true,  0.70],
  ['12', false, 0.70],
  ['13', true,  0.84],
  ['14', false, 0.84],
  ['15', true,  0.91],
  ['16', false, 1.00],
]

const lerp = (a: number, b: number, t: number) => a + (b - a) * t

// ─── Component ────────────────────────────────────────────────────────────────

